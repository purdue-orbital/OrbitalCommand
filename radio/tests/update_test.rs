static SAMPLE_RATE: f32 = 1e5;
static BAUD_RATE: f32 = 1e4;

#[test]
pub fn test_updates() {
    let samples_per_a_symbol = (SAMPLE_RATE / BAUD_RATE) as usize;

    let mut mods = Modulators::new(samples_per_a_symbol, SAMPLE_RATE);
    let mut demods = Demodulators::new(samples_per_a_symbol, SAMPLE_RATE);

    // Preform mod before update
    let ask_mod = mods.ask(BYTES_2048);
    let fsk_mod = mods.fsk(BYTES_2048);
    let bpsk_mod = mods.bpsk(BYTES_2048);
    let qpsk_mod = mods.qpsk(BYTES_2048);

    // Preform demods before update
    let ask_demod = demods.ask(ask_mod.clone());
    let fsk_demod = demods.fsk(fsk_mod.clone());
    let bpsk_demod = demods.bpsk(bpsk_mod.clone());
    let qpsk_demod = demods.qpsk(qpsk_mod.clone());

    // preform updates
    mods.update(samples_per_a_symbol, SAMPLE_RATE);
    demods.update(samples_per_a_symbol, SAMPLE_RATE);

    // run tests
    assert_eq!(ask_mod, mods.ask(BYTES_2048));
    assert_eq!(fsk_mod, mods.fsk(BYTES_2048));
    assert_eq!(bpsk_mod, mods.bpsk(BYTES_2048));
    assert_eq!(qpsk_mod, mods.qpsk(BYTES_2048));

    assert_eq!(ask_demod, demods.ask(ask_mod));
    assert_eq!(fsk_demod, demods.fsk(fsk_mod));
    assert_eq!(bpsk_demod, demods.bpsk(bpsk_mod));
    assert_eq!(qpsk_demod, demods.qpsk(qpsk_mod));
}
